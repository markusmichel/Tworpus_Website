<h1>An Easy-to-Use Tool for the Creation of Tailored Twitter Corpora</h1>
Tworpus is an easy-to-use tool for the creation of tailored Twitter corpora. Tworpus allows scholars to create corpora without having to know about the Twitter Application Programming Interface (API) and related technical aspects. At the same time our tool complies with Twitter’s ”rules of the road” on how to use tweet data. Corpora may be composed in various sizes and for specific scenarios, as the Tworpus interface provides controls for filtering and gathering customized collections of tweets, which may serve as the basis for subsequent analyses.
<br /><br />
For more details see:<br />
Bazo, A., Burghardt, M. & Wolff, C. (2013). <a href="http://link.springer.com/chapter/10.1007/978-3-642-40722-2_3" target="_new">TWORPUS – An Easy-to-Use Tool for the Creation of Tailored Twitter Corpora.</a> In Gurevych, I., Biemann, C. & Zesch, T. (eds.): GSCL 2013, LNAI 8105 „Language Processing and Knowledge in the Web“, pp. 23-34. Springer, Berlin Heidelberg.
